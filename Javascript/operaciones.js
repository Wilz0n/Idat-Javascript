// Operadores de Comparacion

let a = 5;
let b = '5';
console.log(a == b) // el resultado vendria a ser 5 ya que es igual que '5'

//ejemplo de igualdad estricta

let c = 10;
let d = '10';
console.log(c === d) // vendria a ser 'false' oir qye 10 no es igual que '10'

//ejemplo de desigualdad

let e = 20;
let f = '20';
console.log(e != f) // seria 'false' porque 20 no es igual a '20'

//ejemplo de desigualdad estricta

let g = 5;
let h = '5';
console.log(g !== h); // este vendria a ser 'true' porque 5 no es igual a '5' por el tipo de dato

let user = 'Mauri'
let user2 = 'Pereda'

console.log(user !== user2) // viene a ser 'true' porque son diferentes.