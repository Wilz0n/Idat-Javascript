a += b; // a = a + b (a ahora es 15)

//Operadores de Comparación:

let edad = 18;

console.log(edad == 18); // true
console.log(edad === '18'); // false (comparación estricta)
console.log(edad !== 20); // true (distinto de)
console.log(edad > 20); // false
console.log(edad < 20); // true
console.log(edad >= 18); // true
console.log(edad <= 18); // true