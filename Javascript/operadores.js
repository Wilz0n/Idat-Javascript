//Operadores Aritméticos:

let x = 10;
let y = 5;

let suma = x + y; // suma = 15
let resta = x - y; // resta = 5
let multiplicacion = x * y; // multiplicacion = 50
let division = x / y; // division = 2
let modulo = x % y; // modulo = 0 (el resto de la división)

//Operadores de Asignación:

let a = 10;
