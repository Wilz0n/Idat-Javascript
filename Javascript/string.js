let texto = "Hola Mundo";

// length 

console.log(texto.length); // lo imprime 10

// toUpperCase

console.log(texto.toLocaleUpperCase()); // vuelve el texto en mayuscula el string en este caso "HOLA MUNDO"

// toLowerCase

console.log(texto.toLocaleLowerCase()); // vuelve el texto en minuscula el string en este caso "hola mundo"

// trim().length

let parrafo = "       Hola Mundo     " // trim() este se utiliza para eliminar los espacios blancos al principio y al final del mismo 
console.log(parrafo.trim().length);